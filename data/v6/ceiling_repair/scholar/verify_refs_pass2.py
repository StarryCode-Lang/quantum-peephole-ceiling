def normalize(s: str) -> set:
    return set("".join(c for c in s.lower() if c.isalnum() or c.isspace()).split())


def sim(a: str, b: str) -> float:
    ta, tb = normalize(a), normalize(b)
    return len(ta & tb) / len(ta | tb) if ta and tb else 0.0


def run_query(ref_id: str, query: str) -> list[dict]:
    out_csv = RESULTS / f"ref2_{ref_id}.csv"
    params = {"query": query, "file_path": str(out_csv), "num_results": 5, "hl": "en"}
    cmd = ["python3", "scripts/scholar_tool.py", "call",
           "--api-name", "scholar_search",
           "--params-json", json.dumps(params)]
    subprocess.run(cmd, cwd=SCHOLAR, capture_output=True, text=True, timeout=120)
    if not out_csv.exists():
        return []
    with open(out_csv, newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def main():
    rows = []
    for ref_id, title, year, surname in REFS2:
        try:
            results = run_query(ref_id, title)
        except Exception as exc:  # noqa: BLE001
            rows.append({"ref_id": ref_id, "query": title, "status": f"ERROR: {exc}"})
            continue
        best, best_sim = None, 0.0
        for r in results:
            s = sim(title, r.get("title", ""))
            if s > best_sim:
                best, best_sim = r, s
        if best is None:
            status = "NOT_FOUND"
        else:
            byear = str(best.get("year", ""))
            authors = best.get("authors", "")
            year_ok = byear.replace(".0", "").isdigit() and abs(int(float(byear)) - year) <= 2
            author_ok = surname.lower() in authors.lower() if surname else True
            tag = "VERIFIED" if (best_sim >= 0.6 and (year_ok or author_ok)) else \
                  "WEAK_MATCH" if best_sim >= 0.4 else "NOT_FOUND"
            status = (f"{tag} sim={best_sim:.2f} year={byear} "
                      f"authors={authors[:40]} ({best.get('title','')[:70]})")
        rows.append({"ref_id": ref_id, "query": title, "status": status})
        print(f"[{ref_id:>2s}] {status}")
        time.sleep(1.0)

    out = HERE / "ref_verification_pass2.csv"
    tmp = HERE / "ref_verification_pass2.csv.tmp"
    with open(tmp, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=["ref_id", "query", "status"])
        w.writeheader()
        w.writerows(rows)
    tmp.replace(out)
    print(f"\nWrote {out}")


if __name__ == "__main__":
    main()
